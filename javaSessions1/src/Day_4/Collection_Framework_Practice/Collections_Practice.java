/*
* The Collection in Java is a framework that provides an architecture to store and manipulate the group of objects.
* Java Collections can achieve all the operations that you perform on a data such as searching, sorting, insertion, manipulation, and deletion.

* All collections frameworks contain the following:
* Interfaces and its implementations, i.e., classes
* Algorithm

* The Collection interface is the root interface of the Java collections framework.
* However, it is implemented through its subinterfaces like List, Set, and Queue.
* For example, the ArrayList class implements the List interface which is a subinterface of the Collection Interface.

* The Collection framework includes other interfaces as well: Map and Iterator

* One of the main goals why collections framework was designed:
* We do not have to write code to implement these data structures and algorithms manually.
* The framework had to be high-performance.
* The implementations for the fundamental collections (dynamic arrays, linked lists, trees, and hashtables) were to be highly efficient.
 */

